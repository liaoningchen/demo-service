package com.cdy.base.util.bean;

public enum ResultEnum {
    SUCCESS(0,"成功"),
    ERROR(1,"系统异常")
    ;

    ResultEnum(int code, String desc) {
        this.code = code;
        this.desc = desc;
    }

    private int code;
    private String desc;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
